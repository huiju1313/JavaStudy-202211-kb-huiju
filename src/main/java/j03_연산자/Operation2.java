package j03_연산자;

public class Operation2 {
    public static void main(String[] args) {
         int num = 10;

        System.out.println(num);
        System.out.println(++num); // 지금 더해라
        System.out.println(num++);
        System.out.println(num);// 다음 번에 더해라
    }
}

//++num 현재 num에 1을 더해라
//num++ 다음번 num에 1을 더해라
